//The main differences between primitive and non-primitive data types are:
//
//1. Primitive types in Java are predefined and built into the language, while non-primitive types are created by the programmer (except for String).
//
//2. Non-primitive types can be used to call methods to perform certain operations, whereas primitive types cannot.
//
//3. Primitive types start with a lowercase letter (like int), while non-primitive types typically starts with an uppercase letter (like String).
//
//4. Primitive types always hold a value, whereas non-primitive types can be null.
//
